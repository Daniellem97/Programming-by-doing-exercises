using System;

namespace _15._The_forgetful_machine
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.WriteLine("Give me a word!");
            Console.ReadLine();
            Console.WriteLine("Give me a second word!");
            Console.ReadLine();
            Console.WriteLine("Great, now whats your favourite number?");
            Console.ReadLine();
            Console.WriteLine("And your second-favourite number...");
            Console.ReadLine();
            Console.WriteLine("Whew! wasnt that fun?");
        }

    }
}
